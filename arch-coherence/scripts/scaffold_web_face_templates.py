from django.conf import settings
from django.http import HttpResponseNotFound


class FaceHostMiddleware:
    """Attach request.face from settings.FACE; 404 a host bound to the other face."""

    def __init__(self, get_response):
        self.get_response = get_response

    def __call__(self, request):
        request.face = getattr(settings, "FACE", "api")
        host = request.get_host().split(":")[0]
        other_prefix = "mcp." if request.face == "api" else "api."
        if host.startswith(other_prefix):
            return HttpResponseNotFound(b"host does not match this face")
        return self.get_response(request)
