def _app_class(name: str) -> str:
    return "".join(part.capitalize() for part in name.split("_")) + "Config"


def render_project(manifest: dict, out: Path, *, manifest_only: bool = False) -> None:
    """Write the qjango-shaped djapp tree: project/ + apps/<v>/ + apps/mcp.py."""
    (out / "project").mkdir(parents=True, exist_ok=True)
    # docs/api/ holds the generated artifacts: the manifest IR (also what
    # --manifest-only emits), the OpenAPI doc, and the human/LLM endpoint list.
    docs_api = out / "docs" / "api"
    docs_api.mkdir(parents=True, exist_ok=True)
    (docs_api / "manifest.json").write_text(json.dumps(manifest, indent=2) + "\n")
    if manifest_only:
        return

    # project/ (composition root)
    proj = out / "project"
    (proj / "__init__.py").write_text("")
    # settings/ package: shared base + one module per face
    settings_pkg = proj / "settings"
    settings_pkg.mkdir(parents=True, exist_ok=True)
    (settings_pkg / "__init__.py").write_text("")
    (settings_pkg / "settings.py").write_text(_settings_py(manifest))
    (settings_pkg / "api.py").write_text(SETTINGS_API_PY)
    (settings_pkg / "mcp.py").write_text(SETTINGS_MCP_PY)
    # urls/ package: one urlconf per face (selected by each settings module)
    urls_pkg = proj / "urls"
    urls_pkg.mkdir(parents=True, exist_ok=True)
    (urls_pkg / "__init__.py").write_text("")
    (urls_pkg / "apiurls.py").write_text(_urls_api_py(manifest))
    (urls_pkg / "mcpurls.py").write_text(URLS_MCP_PY)
    (proj / "faceguard.py").write_text(FACEGUARD_PY)
    (proj / "asgi.py").write_text(ASGI_PY)
    (proj / "wsgi.py").write_text(WSGI_PY)
    (proj / "views.py").write_text(VIEWS_PY)
    (proj / "sitemaps.py").write_text(_sitemaps_py(manifest))

    # apps/<vertical>/ (one Django app per vertical; both faces co-located)
    for v in manifest["verticals"]:
        app = out / "apps" / v["app"]
        (app / "views").mkdir(parents=True, exist_ok=True)
        (app / "urls").mkdir(parents=True, exist_ok=True)
        (app / "__init__.py").write_text("")
        (app / "apps.py").write_text(
            APP_CONFIG_PY.format(cls=_app_class(v["app"]), name=f"apps.{v['app']}")
        )
        (app / "models.py").write_text(EMPTY_MODELS_PY)
        (app / "admin.py").write_text(EMPTY_ADMIN_PY)
        (app / "commands.py").write_text(_commands_py(v))
        (app / "views" / "__init__.py").write_text("")
        (app / "views" / "apiviews.py").write_text(_apiviews_py(v))
        (app / "views" / "mcpviews.py").write_text(_mcpviews_py(v))
        (app / "urls" / "__init__.py").write_text("")
        (app / "urls" / "apiurls.py").write_text(_apiurls_py(v))
    (out / "apps" / "__init__.py").write_text("")

    # apps/mcp.py — the single MCP endpoint, aggregating every vertical's mcpviews
    (out / "apps" / "mcp.py").write_text(_mcp_endpoint_py(manifest))

    # docs/api/ — the OpenAPI spec + the human/LLM endpoint list (api face).
    # docs_api was created (with manifest.json) at the top of render_project.
    (docs_api / "openapi.json").write_text(_openapi_doc(manifest))
    (docs_api / "ENDPOINTS.md").write_text(_endpoints_md(manifest))

    # top-level files
    (out / "manage.py").write_text(MANAGE_PY)
    (out / "pyproject.toml").write_text(PYPROJECT_TOML)
    (out / "run.sh").write_text(RUN_SH)
    (out / "run.sh").chmod(0o755)
    apache = out / "apache"
    apache.mkdir(parents=True, exist_ok=True)
    (apache / "api.vhost.conf").write_text(APACHE_API_VHOST)
    (apache / "mcp.vhost.conf").write_text(APACHE_MCP_VHOST)
