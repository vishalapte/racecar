def _openapi_doc(manifest: dict) -> str:
    """OpenAPI 3.1 document for the REST face, built from the manifest. GET commands
    expose query parameters; non-GET commands take the input_schema as a JSON body."""
    paths: dict = {}
    tags = []
    seen = set()
    for v in manifest["verticals"]:
        tag = v["app"]
        if tag not in seen:
            seen.add(tag)
            tags.append({"name": tag, "description": f"{v['vertical']} endpoints"})
        for c in v["commands"]:
            method = c["method"].lower()
            schema = c["input_schema"]
            op = {
                "operationId": c["mcp_tool"],
                "summary": c["description"] or c["subcommand"],
                "tags": [tag],
                "responses": {"200": {"description": "Success"}},
            }
            if c["is_write"]:
                op["responses"]["403"] = {
                    "description": "Writes disabled (RACECAR_WEB_FACE_ALLOW_WRITES off)"
                }
            if method == "get":
                props = schema.get("properties", {})
                required = set(schema.get("required", []))
                op["parameters"] = [
                    {"name": k, "in": "query", "required": k in required, "schema": s}
                    for k, s in props.items()
                ]
            else:
                op["requestBody"] = {
                    "required": bool(schema.get("required")),
                    "content": {"application/json": {"schema": schema}},
                }
            paths.setdefault(c["http_path"], {})[method] = op
    doc = {
        "openapi": "3.1.0",
        "info": {
            "title": f"{manifest['package']} REST API",
            "version": "1.0.0",
            "description": (
                "Generated by racecar-deploy. All routes are under /api/v1/. Write "
                "verbs (any non-GET) are gated by RACECAR_WEB_FACE_ALLOW_WRITES and "
                "return 403 when off. The MCP face is a separate server at /mcp."
            ),
        },
        "servers": [{"url": "/", "description": "This host"}],
        "tags": tags,
        "paths": paths,
    }
    return json.dumps(doc, indent=2) + "\n"


